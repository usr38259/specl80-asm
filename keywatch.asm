PUTC	EQU	0C037H
INP	EQU	0C337H
PUTSTR	EQU	0C438H

	MVI	C, 0AH
	CALL	PUTC
NX:	CALL	INP
	STA	CHR
	MOV	C, A
	RRC
	RRC
	RRC
	RRC
	ANI	0FH
	ORI	30H
	CPI	3AH
	JC	NHX1
	ADI	7
NHX1:	LXI	H, S
	MOV	M, A
	INX	H
	MOV	A, C
	ANI	0FH
	ORI	30H
	CPI	3AH
	JC	NHX2
	ADI	7
NHX2:	MOV	M, A
	DCX	H
	CALL	PUTSTR
	JMP	NX

S	DB	'00: ', 027H
CHR	DB	020H, 027H, 0AH, 0

	ENDALL
